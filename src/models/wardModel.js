class Ward {
  constructor(
    bedCount,
    createdAt,
    facilityId,
    floorNumber,
    name,
    type,
    updatedAt,
    wardId,
    wardNumber
  ) {
    this.bedCount = bedCount;
    this.createdAt = createdAt;
    this.facilityId = facilityId;
    this.floorNumber = floorNumber;
    this.name = name;
    this.type = type;
    this.updatedAt = updatedAt;
    this.wardId = wardId;
    this.wardNumber = wardNumber;
  }
}

export default Ward;
